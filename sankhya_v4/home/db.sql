-- Tabela de Parâmetros configuráveis do sistema qualidade
CREATE TABLE PARAMETROS (
    ID               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME             VARCHAR2(100) NOT NULL,
    VALOR            VARCHAR2(100) NOT NULL,
    DESCRICAO        VARCHAR2(255),
    DATA_CRIACAO     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DATA_ATUALIZACAO TIMESTAMP
);


-- Registros de umidade do produto por palete
CREATE TABLE UMIDADE_PRODUTO (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMERO_PALETE VARCHAR2(50) NOT NULL,
    VALOR_UMIDADE NUMBER(5,2) NOT NULL,
    STATUS        VARCHAR2(20),
    DATA_HORA     TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


-- Medições da estufa
CREATE TABLE MEDICAO_ESTUFA (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMERO_PALETE VARCHAR2(50),
    TEMPERATURA   NUMBER(5,2),
    STATUS        VARCHAR2(20),
    DATA_HORA     TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Log de verificações dos motores
CREATE TABLE LOG_MOTORES (
    ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MOTOR_ID  NUMBER NOT NULL,
    STATUS    VARCHAR2(20) CHECK (STATUS IN ('LIGADO', 'DESLIGADO', 'ERRO')),
    DATA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_LOG_MOTOR FOREIGN KEY (MOTOR_ID) REFERENCES MOTORES(ID)
);


-- Tabela de motores cadastrados
CREATE TABLE MOTORES (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME         VARCHAR2(50),
    MODULO       NUMBER,
    LADO         VARCHAR2(10),
    DATA_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


-- Registros de temperatura do secador
CREATE TABLE TEMPERATURA_SECADOR (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TEMPERATURA NUMBER(5,2) NOT NULL,
    STATUS      VARCHAR2(20),
    DATA_HORA   TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabela de carrinhos
CREATE TABLE CARRINHOS (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMERO       VARCHAR2(50) UNIQUE NOT NULL,
    DATA_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabela de paletes
CREATE TABLE PALETES (
    ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODIGO_PALETE VARCHAR2(50) UNIQUE NOT NULL,
    DATA_CRIACAO  TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);









-- Movimentação dos carrinhos no secador
CREATE TABLE MOVIMENTACAO_CARRINHOS (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CARRINHO_ID         NUMBER NOT NULL,
    DATA_HORA_ENTRADA   TIMESTAMP NOT NULL,
    DATA_HORA_SAIDA     TIMESTAMP,
    QUANTIDADE_BANDEJAS NUMBER,
    PESO_KG             NUMBER(10,2),
    TEMPO_TOTAL_HORAS   NUMBER(10,2),
    STATUS              VARCHAR2(20),
    DATA_CRIACAO        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_CARRINHO FOREIGN KEY (CARRINHO_ID) REFERENCES CARRINHOS(ID)
);

-- ============================================================
-- TRIGGERS
-- ============================================================

-- Calcula o tempo total de permanência ao registrar saída
CREATE OR REPLACE TRIGGER TRG_CALC_TEMPO_CARRINHO
BEFORE UPDATE ON MOVIMENTACAO_CARRINHOS
FOR EACH ROW
WHEN (NEW.DATA_HORA_SAIDA IS NOT NULL)
BEGIN
    :NEW.TEMPO_TOTAL_HORAS :=
        (CAST(:NEW.DATA_HORA_SAIDA AS DATE) - CAST(:NEW.DATA_HORA_ENTRADA AS DATE)) * 24;
END;
/

-- Define status CONCLUIDO ou ATRASADO com base no parâmetro TEMPO_SECAGEM_HORAS
CREATE OR REPLACE TRIGGER TRG_STATUS_CARRINHO
BEFORE UPDATE ON MOVIMENTACAO_CARRINHOS
FOR EACH ROW
WHEN (NEW.DATA_HORA_SAIDA IS NOT NULL)
DECLARE
    V_LIMITE NUMBER;
BEGIN
    SELECT TO_NUMBER(VALOR) INTO V_LIMITE
      FROM PARAMETROS
     WHERE NOME = 'TEMPO_SECAGEM_HORAS';

    IF :NEW.TEMPO_TOTAL_HORAS > V_LIMITE THEN
        :NEW.STATUS := 'ATRASADO';
    ELSE
        :NEW.STATUS := 'CONCLUIDO';
    END IF;
END;
/

